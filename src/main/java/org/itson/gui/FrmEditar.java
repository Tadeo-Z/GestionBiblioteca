    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
       int filaSeleccionada = tablaUsuarios.getSelectedRow();

    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un usuario para editar", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Obtener los valores de la fila seleccionada en la tabla
    Long id = (Long) tablaUsuarios.getValueAt(filaSeleccionada, 0);
    String nuevoNombre = jTextFieldNombre.getText().trim();
    String nuevaContrasena = jTextFieldContrasena.getText().trim();

    // Verificar que el ID no sea nulo
    if (id == null || id <= 0) {
        JOptionPane.showMessageDialog(this, "El ID del usuario no es válido", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Creamos el objeto Usuario con los datos actualizados y el ID correcto
    Usuario usuario = new Usuario();
    usuario.setId(id); // Establecer el ID del usuario a editar
    usuario.setNombre(nuevoNombre);
    usuario.setContrasena(nuevaContrasena);

    try {
        usuarioNegocio.editar(usuario);
        JOptionPane.showMessageDialog(this, "Usuario editado exitosamente");
        mostrarUsuariosEnTabla();
        limpiarCampos();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al editar usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonEditarActionPerformed

