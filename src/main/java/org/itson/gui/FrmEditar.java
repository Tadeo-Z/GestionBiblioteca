   private void mostrarUsuariosEnTabla() {
    try {
        List<Usuario> usuarios = usuarioNegocio.getAllUsuarios();
        DefaultTableModel model = (DefaultTableModel) tablaUsuarios.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de agregar usuarios

        // Crear un nuevo DefaultTableModel no editable
        DefaultTableModel newModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Todas las celdas no son editables
            }
        };

        // Copiar las columnas del modelo original al nuevo modelo no editable
        for (int i = 0; i < model.getColumnCount(); i++) {
            newModel.addColumn(model.getColumnName(i));
        }

        // Agregar filas al nuevo modelo no editable
        for (Usuario usuario : usuarios) {
            Object[] row = {usuario.getId(), usuario.getNombre(), usuario.getContrasena()};
            newModel.addRow(row);
        }

        // Establecer el nuevo modelo no editable en la tabla
        tablaUsuarios.setModel(newModel);
    } catch (Exception e) {
        // Manejar cualquier excepción que pueda ocurrir al obtener los usuarios
        e.printStackTrace();
    }
}

    private void limpiarCampos() {
        jTextFieldId.setText("");
        jTextFieldNombre.setText("");
        jTextFieldContrasena.setText("");
    }

