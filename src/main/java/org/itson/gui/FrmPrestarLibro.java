    private void btnPrestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrestarActionPerformed
        // TODO add your handling code here:
        try {
            UsuarioNegocio unegocio = new UsuarioNegocio();
            
           
            List<Libro> librosPrestamo = obtenerLibrosTabla(tablaEspera);
      
            // Obtener el usuario seleccionado en la lista de usuarios
            String nombreUsuario = (String) listaUsuarios.getSelectedItem();
            Usuario usuarioSeleccionado = unegocio.getUsuarioByName(nombreUsuario);

            // Verificar que se haya seleccionado un usuario
            if (usuarioSeleccionado == null) {
                mostrarAlerta("Por favor, selecciona un usuario.");
                return;
            }
            
            Prestamo prestamo = new Prestamo(librosPrestamo, usuarioSeleccionado, EstadoPrestamo.PRESTADO);
            pmNegocio.registrarPrestamo(prestamo);

        
            JOptionPane.showMessageDialog(this, "Préstamo realizado con éxito.");
            esperaTableModel.setRowCount(0);
            
        } catch (Exception ex) {
            mostrarAlerta("Error al realizar el préstamo: " + ex.getMessage());
        }
        
        this.dispose();
        FrmPrincipal frm = new FrmPrincipal();
        frm.setVisible(true);
    }//GEN-LAST:event_btnPrestarActionPerformed

