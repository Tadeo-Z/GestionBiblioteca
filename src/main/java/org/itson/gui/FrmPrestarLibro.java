    private List<Libro> obtenerLibrosTabla(JTable tabla) throws Exception {
        LibroNegocio lnegocio = new LibroNegocio();
        List<Libro> libros = new ArrayList<>();
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();

        for (int i = 0; i < modelo.getRowCount(); i++) {
               
            String isbn = (String) modelo.getValueAt(i, 2);     
            Libro libro = lnegocio.obtenerLibroISBN(isbn);
            libros.add(libro);
        }

        return libros;
    }
    
    public void inicializarTablas() throws Exception{
        librosTableModel = new DefaultTableModel();
        librosTableModel.addColumn("Título");
        librosTableModel.addColumn("Autor");
        librosTableModel.addColumn("ISBN");
        librosTableModel.addColumn("Estado");

        librosTabla.setModel(librosTableModel);
        obtenerLibros();
        
        esperaTableModel = new DefaultTableModel();
        esperaTableModel.addColumn("Título");
        esperaTableModel.addColumn("Autor");
        esperaTableModel.addColumn("ISBN");
        esperaTableModel.addColumn("Estado");

        tablaEspera.setModel(esperaTableModel);
    }
    
    public void obtenerLibros() throws Exception{
        LibroNegocio lbNegocio = new LibroNegocio();
        List<Libro> librosDisponibles = lbNegocio.obtenerLibrosDisponibles();
        for (Libro libro : librosDisponibles) {
            Object[] rowData = {libro.getTitulo(), libro.getAutor(), libro.getIsbn(), libro.isDisponibilidad()};
            librosTableModel.addRow(rowData);
        }
    }
    
   
    public void obtenerUsuarios() throws Exception {
        UsuarioNegocio uNegocio = new UsuarioNegocio();
        List<Usuario> usuarios = uNegocio.getAllUsuarios();

        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        listaUsuarios.setModel(model);

        for (Usuario usuario : usuarios) {
            model.addElement(usuario.getNombre());
        }
    }
    
    private void moverFilaEntreTablas(javax.swing.JTable tablaOrigen, DefaultTableModel modeloDestino) {
        int filaSeleccionada = tablaOrigen.getSelectedRow();
        if (filaSeleccionada == -1) {
            mostrarAlerta("Por favor, selecciona una fila para mover.");
            return;
        }

        // Obtener el modelo de la tabla de origen
        DefaultTableModel modeloOrigen = (DefaultTableModel) tablaOrigen.getModel();

        // Obtener datos de la fila seleccionada en tablaOrigen
        Vector rowData = (Vector) modeloOrigen.getDataVector().get(filaSeleccionada);

        // Agregar fila a modeloDestino
        modeloDestino.addRow(rowData);

        // Eliminar fila seleccionada de modeloOrigen
        modeloOrigen.removeRow(filaSeleccionada);
    }
    
    private void mostrarAlerta(String mensaje) {
        JOptionPane.showMessageDialog(this, mensaje, "Alerta", JOptionPane.ERROR_MESSAGE);
    }
    

    
